def azimuthsGen(nFrames):
    azimuths_deg = np.linspace(0, 360, nFrames + 1)[1:] % 360
    azimuths_rad = [np.deg2rad((a - azimuths_deg[-1]) % 360) for a in azimuths_deg]
    azimuths_rad[:-1].sort()

    return azimuths_rad, [1.5707963267948966] * nFrames


def orbitalCameraPoseGen(vidsDir, saveDir, nFrms):
    # assume azimuths are uniformly sampled; the elevation remains 0; anti-clockwise camera as default
    azimuths_rad, polars_rad = azimuthsGen(nFrms)

    # define the directory to save camera pose annotations
    if not os.path.exists(saveDir): os.makedirs(saveDir)

    # generate text files containing frame IDs, azimuths and elevations
    vidIDs = os.listdir(vidsDir)
    if ".DS_Store" in vidIDs: vidIDs.remove(".DS_Store")
    for vid_id in vidIDs:
        txtFile = open(os.path.join(saveDir, vid_id + ".txt"), 'w')
        frmIDs = os.listdir(os.path.join(vidsDir, vid_id))
        if ".DS_Store" in frmIDs: frmIDs.remove(".DS_Store")
        frmIDs = sorted(frmIDs)
        for idx in range(nFrms):
            a_val, e_val = azimuths_rad[idx], polars_rad[idx]
            txtFile.write((vid_id + " " + frmIDs[idx][:-4] + " " + str(a_val) + " " + str(e_val) + "\n"))
        txtFile.close()
        print(vid_id + " labeled w/ orbital camera pose recorded;")
    print("Done.")